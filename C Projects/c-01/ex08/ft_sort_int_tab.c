void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	x;

	i = 1;
	while (i < size)
	{
		x = tab[i];
		j = i - 1;
		while (j >= 0 && tab[j] > x)
		{
			tab[j + 1] = tab[j];
			j--;
		}
		tab[j + 1] = x;
		i++;
	}	
}
