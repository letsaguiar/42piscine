#ifndef FT_BOOLEAN_H
#define ABS(Value)                                     	\
({                                                      \
    int abs;                                       		\
    if (Value < 0) {                         			\
         abs = Value *- 1;                              \
    } else {                                            \
		abs = Value;									\
    }                                                   \
    abs;                                             	\
})

#endif
